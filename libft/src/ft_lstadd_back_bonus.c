#include "../includes/libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (!lst)
		return ;
	if (!*lst)
		*lst = new;
	else if (lst)
	{
		tmp = ft_lstlast(*lst);
		tmp->next = new;
	}
}
